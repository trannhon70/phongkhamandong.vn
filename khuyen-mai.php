    <script>
        function showToast(message, type = 'info') {
            const toastContainer = document.getElementById('toast-container');
            if (!toastContainer) return;
            // Create toast element
            const toast = document.createElement('div');
            toast.classList.add('toast', type);
            toast.innerHTML = ` ${message}`;
            // Append toast to container
            toastContainer.appendChild(toast);
            // Remove toast after 3 seconds
            setTimeout(() => {
                toast.remove();
            }, 3000);
        }
        document.addEventListener('DOMContentLoaded', () => {
            // Lấy tất cả các form
            const forms = document.querySelectorAll('.consultation__container-row-left-body');

            // Duyệt qua từng form
            forms.forEach((form) => {
                const nameInput = form.querySelector('input[type="text"]');
                const phoneInput = form.querySelector('input[type="number"]');
                const submitButton = form.querySelector('.consultation__container-row-left-body-button img');

                // Gắn sự kiện click cho nút trong từng form
                submitButton.addEventListener('click', () => {
                    const nameValue = nameInput.value.trim();
                    const phoneValue = phoneInput.value.trim();
                    const url = new URL(window.location.toLocaleString());
                    if (!nameValue || !phoneValue) {
                        showToast("Vui lòng điền đầy đủ thông tin trong form này!", 'warning');
                        return;
                    }

                    let formData = {
                        sdt: phoneValue,
                        hoten: nameValue,
                        url: url.href
                    };

                    if (formData.sdt !== '') {
                        const xhr = new XMLHttpRequest();
                        xhr.open("POST", "<?php echo $local ?>/api/khach-hang/create-user.php", true); // PHP local variable
                        xhr.setRequestHeader("Content-Type", "application/json;charset=UTF-8");

                        xhr.onreadystatechange = function() {
                            if (xhr.readyState === 4 && xhr.status === 200) {
                                let response = JSON.parse(xhr.responseText);
                                if (response.status === 'success') {
                                    showToast(response.message, 'success');
                                    // Reset giá trị đúng cách
                                    phoneValue.value = '';
                                    nameValue.value = '';
                                } else {
                                    showToast(response.message, 'error');
                                }
                            }
                        };

                        xhr.send(JSON.stringify(formData));
                    } else {
                        showToast('Số điện thoại không được bỏ trống!', 'warning');
                    }


                });
            });
        });
    </script>
